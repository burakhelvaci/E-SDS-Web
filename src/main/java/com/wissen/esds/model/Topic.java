package com.wissen.esds.model;

public class Topic {
    private String topic;
    private String message;

    public String getTopic() {
        return topic;
    }

    public void setTopic(String topic) {
        this.topic = topic;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String data) {
        this.message = data;
    }
    
    
}
